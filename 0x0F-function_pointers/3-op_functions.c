#include "3-calc.h"

/**
 * op_add - Function for addition of two integers
 * @a: a number
 * @b: a number
 *
 * Return: sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function for subtraction of two integers
 * @a: a number
 * @b: a number
 *
 * Return: sum of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function for multiplication of two integers
 * @a: a number
 * @b: a number
 *
 * Return: sum of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function for division of two integers
 * @a: a number
 * @b: a number
 *
 * Return: sum of a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function for determining the mod of two numbers
 * @a: a number
 * @b: a number
 *
 * Return: sum of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
